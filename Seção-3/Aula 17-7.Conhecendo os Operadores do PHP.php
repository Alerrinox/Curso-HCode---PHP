<?php

	// Operadores incremental (++)
	$a = 10;
	// Existe o pré incremento que vem na frente da expressão e soma mais 1 antes do começo da resolução (ex: ++$a).
	echo ++$a;
	// Existe o pro incremento que vem na atrás da expressão e soma mais 1 depois do termino da resolução (ex: $a++).
	echo "<BR/>";

	// Tem o incremento e o decremento. O decremento subtrai 1 usando a expressão (--). 
?>